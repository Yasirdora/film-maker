#!/usr/bin/env node
/**
 * Build-time manifest generator for the SpatialCarousel.
 *
 * Scans public/assets/carousel/ for .mp4/.mov/.webm files and writes
 * components/landing-blocks/sections/carousel-manifest.ts with the
 * slide list. Runs as a `prebuild` hook so adding clips to the folder
 * is a zero-code-change operation:
 *
 *   1. Drop new files into public/assets/carousel/
 *   2. Run `npm run dev` or `npm run build`
 *   3. The manifest regenerates and the carousel picks them up
 *
 * The carousel still samples a fixed-size ring out of this manifest at
 * runtime (RING_LENGTH cards from N total clips), so adding more clips
 * grows the variety pool without growing the DOM.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");

const SRC_DIR = path.join(projectRoot, "public/assets/carousel");
const WEB_DIR = path.join(SRC_DIR, "web");
const POSTER_DIR = path.join(SRC_DIR, "posters");
const OUT_FILE = path.join(
    projectRoot,
    "components/landing-blocks/sections/carousel-manifest.ts",
);

const VIDEO_EXT = /\.(mp4|mov|webm)$/i;

/** Turn `Neon Street.mp4` → `Neon Street`. Preserves case. */
function humanLabel(filename) {
    return filename
        .replace(/\.[^.]+$/, "")
        .replace(/[-_]+/g, " ")
        .trim();
}

/** Turn `Neon Street.mp4` → `neon-street` (slug for stable React keys). */
function slugId(filename) {
    return filename
        .replace(/\.[^.]+$/, "")
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, "-")
        .replace(/^-+|-+$/g, "");
}

if (!fs.existsSync(SRC_DIR)) {
    console.error(
        `carousel-manifest: source directory missing — ${SRC_DIR}`,
    );
    process.exit(1);
}

const files = fs
    .readdirSync(SRC_DIR)
    .filter((f) => VIDEO_EXT.test(f))
    .sort();

/** Pair each source clip with its optimized .mp4 + poster .jpg when present.
 *  Falls back to the original file when the optimizer hasn't run yet. */
const slides = files.map((file) => {
    const stem = file.replace(VIDEO_EXT, "");
    const webPath = path.join(WEB_DIR, `${stem}.mp4`);
    const posterPath = path.join(POSTER_DIR, `${stem}.webp`);

    return {
        id: slugId(file),
        video: fs.existsSync(webPath)
            ? `/assets/carousel/web/${stem}.mp4`
            : `/assets/carousel/${file}`,
        poster: fs.existsSync(posterPath)
            ? `/assets/carousel/posters/${stem}.webp`
            : null,
        alt: humanLabel(file),
    };
});

const banner = `// AUTO-GENERATED by scripts/generate-carousel-manifest.mjs — do not edit.
// Drop clips into public/assets/carousel/ and run \`npm run carousel:manifest\`
// (or any build) to regenerate.
`;

const body = `export interface CarouselSlide {
    id: string;
    /** Optimized 720p .mp4 when available; otherwise the original master. */
    video: string;
    /** First-frame JPG for instant visual fill while video data loads.
     *  Null when the optimizer hasn't run for this clip yet. */
    poster: string | null;
    alt: string;
}

export const CAROUSEL_SLIDES: readonly CarouselSlide[] = ${JSON.stringify(slides, null, 4)};
`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, `${banner}\n${body}`);

console.log(
    `carousel-manifest: wrote ${slides.length} slide${slides.length === 1 ? "" : "s"} → ${path.relative(projectRoot, OUT_FILE)}`,
);
